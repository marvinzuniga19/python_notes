lista = [ 'autos', 'Motos', 'Patinetas', 'bisicletas', 'monopatines',]

# Metodo reverse hace que los elementos de la lista se mueva de atras hacia adelante

lista.reverse()
print(lista)

# Metodo sort, este da un orden determinado, pero solo ordena si lleva datos de un solo tipo en este caso str. Si la lista llevas str y int no podria ordenar la lista

lista.sort()
print(lista)