# Metodo de upper, convierte la palabra a mayuscula

animales = " PeRros felices "
print(animales.upper())

# Lower, convierte toda la palabra a miniscula
print(animales.lower())

# capitalize, convierte el primer caracter en mayuscula
print(animales.strip().capitalize()) 

# title, convierte en mayuscula el primer caracter de cada letra
print(animales.title())

#  strip, remueve los espacios de los string tanto derecha e isquierda
print(animales.strip())

print(animales.lstrip()) # saca los espacios de la izquierda

print(animales.rstrip()) # saca los espacios de la derecha.


print(animales.find("s")) # busca cadena de caracteresy regresa el indice


print(animales.replace("P", "y"))

