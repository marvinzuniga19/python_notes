# Eliminar elementos dentro de una lista

lista = [
  'perro', 'gato', 'aves', 'alces', 'venados', 'linces'
]

# Metodo Pop, este elimina el ultimo valor de la lista

lista.pop()
# print(lista)

# Metodo remove, este elimina un elemento en particular

lista.remove('aves')
print(lista) # Aca se elimino el str aves y tambien se elimino linces por el metodo pop arriba aplicado