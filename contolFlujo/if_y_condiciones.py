# Sentencia if se utiliza en un bloque de codigo si, y si solo si se cumple una condicion

# Ejemplo:


# a == b  evalua si ambas variables son iguales

if 2 == 2:
    print("2 es igual a 2")

if 2 == 3:
    print("2 es igual a 3")

# a > b   evalua si la variable a es mayor a b

if 3 > 1:
    print("3 es mayor a 1")

if 3 > 3:
    print("3 es mayor a 3")


# a != b  evalua si una variable es distnta a otra
if 2 != 2:
    print("2 es distinto a 2")

if 3 != 2:
    print("3 es distinto a 2")

# a <= b  evalua si una variable es menor o igual
if 2 <= 2:
    print("2 es menor o igual a 2")

if 3 <= 4:
    print("3 es menor o igual a 4")

# a >= b  evalua si una variable es mayor o igual
if 2 >= 2:
    print("2 es menor o igual a 2")

if 3 >= 4:
    print("3 es menor o igual a 4")


# a < b   evalua si una variable es menor a la otra

if 2 < 4:
    print("2 es menor que 4")
else:
    print("2 no es mayor a 4")
